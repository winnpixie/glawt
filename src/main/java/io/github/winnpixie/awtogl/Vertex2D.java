package io.github.winnpixie.awtogl;

public class Vertex2D {
    private final double x;
    private final double y;

    public Vertex2D(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
